import javax.swing.*;

public class Uppgift51{
	public static void main(String[] args){ 
        
        int n, i, summa;
        summa = 0;
        i = 0;
        n = 0;
        
        n = Integer.parseInt(JOptionPane.showInputDialog("n?"));
        while(i <= n){
            
            summa 
            i = i + 1; 
           
        }
         JOptionPane.showMessageDialog(null, "Summan är: " + summa);
       
    }
}